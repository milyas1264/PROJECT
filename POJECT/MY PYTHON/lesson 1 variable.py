# Hello World Program
print("Hello, World!")  # This code prints "Hello, World!"

# Basic Comments
# This is a comment
print("This code is running")  # Prints a message

# Variables and Data Types
name = "Ali"  # String
age = 25  # Integer
my_integer = 42  # Integer
my_float = 3.14159  # Float
my_string = "Python"  # String
my_boolean = False  # Boolean

# Type Conversion (Casting)
num = 7
num_str = str(num)  # Convert integer to string
float_num = 5.67
int_num = int(float_num)  # Convert float to integer

# Checking the data type of a variable
my_var = "Learning Python"
print(type(my_var))  # Output: <class 'str'>

# Adding two integers
num1, num2 = 10, 5
sum_result = num1 + num2
print("Sum:", sum_result)  # Output: Sum: 15

# Concatenating strings
first_name, last_name = "Ali", "Khan"
full_name = first_name + " " + last_name
print("Full Name:", full_name)  # Output: Full Name: Ali Khan

# Using boolean in a condition
is_raining = True
print("Take an umbrella!" if is_raining else "Enjoy the sunshine!")

# Float division
division_result = 10 / 3
print("Division Result:", division_result)  # Output: 3.3333...

# Integer division
int_division_result = 10 // 3
print("Integer Division Result:", int_division_result)  # Output: 3

# Boolean example
is_greater = num1 > num2
print("Is num1 greater than num2?", is_greater)  # Output: True

# Using 'type()' function
print(type(my_integer))  # Output: <class 'int'>
print(type(my_float))  # Output: <class 'float'>
print(type(my_boolean))  # Output: <class 'bool'>
